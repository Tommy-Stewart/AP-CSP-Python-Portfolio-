def core_game():
    global day
    global level
    global name
    print("welcome to pokemon!")
    while True:
        print(f"it is day {day} and you have a level {level} {name}, he has leveled up due to training his dampness")
        question = input("What would you like to do today?(train, rest, battle, or leave) ")
        if question == "train":
            day = day + 1
            level = level + 1
            if level >= 6 and name == "poliwag":
                print("your poliwag has evolved into a poliwhirl")
                level_2()
                level = 1
                name = "poliwhirl"
            elif level >= 6 and name == "poliwhirl":
                print("your poliwhirl has evolved into a poliwrath")
                level_3()
                level = 1
                name = "poliwrath"
            elif level >= 15:
                print("you have reached the max level, do the final battle to complete the game")
        elif question == "rest":
            day = day + 1
            print(f"It is day {day} and you have a level {level} {name}")
        elif question == "leave":
            print("thank you for playing!")
            break
        elif question == "battle":
            bat = input("would you like to do a gym_battle or the final_boss_battle? ")
            if bat == "gym_battle":
                odds = random.randint(1,100)
                if odds <= 70:
                    print("congrats you have won a gym battle, your pokemon has leveled up 2 points")
                    level = level + 2
                if odds >= 70:
                    print("sorry you have lost a gym battle, your pokemon has not leveled up or down")
            if bat == "final_boss_battle":
                odds = random.randint(1,100)
                if level <= 5:
                    if odds <= 10:
                        print("congrats you have won the final boss battle and you have completed the game")
                        break
                    if odds >= 10:
                        print("sorry you have lost the final boss battle and you have lost the game, please try again another time")
                        break
                if level <= 10:
                    if odds <= 25:
                        print("congrats you have won the final boss battle and you have completed the game")
                        break
                    if odds >= 25:
                        print("sorry you have lost the final boss battle and you have lost the game, please try again another time")
                        break
                if level <= 15:
                    if odds <= 50:
                        print("congrats you have won the final boss battle and you have completed the game")
                        break
                    if odds >= 50:
                        print("sorry you have lost the final boss battle and you have lost the game, please try again another time")
                        break

core_game()

